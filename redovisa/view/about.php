<h1>Om webbplatsen</h1>

<p>Denna webbplatsen är en del av kursen <a href="https://dbwebb.se/kurser/databas-v1/">Databasteknologier</a>. Kursen Databasteknologier för webben, a.k.a. databas, och syftet är att vi studenter ska förstå och lära oss modellera och implementera en databas samt utveckla en webbapplikation som använder databasen. Kursen går vid Blekinge Tekniska Högskola VT-20 och är 7,5hp. Som applikationsspråk används serverbaserad JavaScript i webbmiljö.</p>

<p>Kursen omfattar följande områden, databasteknik, relationsmodellen och alternativa modeller, databasmodellering, SQL, SQL med lagrade procedurer, funktioner & triggers, applikationsprogrammering med databaser, applikationsprogrammering i webbmiljö med programmeringsspråket JS på serversidan med Node.js med inslag av HTML och CSS, Verktyg och tekniker för utveckling och test.</p>

