<p class="report__question">2.3 - Hur bekant är du med PHP och programmering rent allmänt, hur känns det att utveckla med PHP?</p>
<p class="report__answer">Jag har ingen tidigare erfarenhet av PHP men det känns väldigt intressant och givande att bli introducerad till utveckling med PHP. Jag kan redan nu se att det finns många fler funktioner med php och precis som tidigare beskrivet i kursmaterialet kan jag se glimtar av vad som verkar vara ett brett programmeringsspråk. Utvecklingsmässigt känns det utmanande, vilket jag tycker är bra. Saker tar tid att lära sig men när det faller på plats och resultatet förvånar en själv blir jag taggad på mer. Min utmaning just nu ligger delvis i tolkning/skrivning av syntax för att uppnå det jag tänker att jag ska uppnå.</p>

<p class="report__question">2.4 - Vad tyckte du om PHP-labben, enkel eller utmanande?</p>
<p class="report__answer">Labben var utmanande och mycket rolig. Jag hade några missförstånd på vägen som gjorde att det tog mer tid än väntat. Exempelvis så trodde jag att beskrivningen i 1.4 bad att få med tecknen " runt strängen pga beskrivningen som var (create the string '"There are 369 chicken's doing some wind."'... ). I min förståelse handlade uppgiften om att använda sig av strängkonkatenering och escapetecken för utskrivning av just " och det resulterade i tidsförlust tills jag förstod att så var inte fallet och då blev det mycket lättare. Vidare tyckte jag uppgifterna för while- och for-looparna var mycket bra och givande. På while-loopen missförstod jag även uppgiften till en början och trodde att jag skulle ta ut antalet ggr som värdet 7.69 skulle subtraheras från 608 tills återstående ligger mellan 28 och 38. Här förlorade jag inte lika mycket tid eftersom jag sedan tidigare moment förstod att det kunde vara min tolkning av uppgiften som var problemet men det krävdes att tänka om vilket slutligen gav sig. Både i while- och for-loopen fick jag en riktig funderare på formulering av syntax vilket var nyttigt och jag tror att min förståelse av iterationer har ökat även om det är nyttigt med mer träning. En besvikelse för min egen del var att jag inte hade tid att försöka mig på extra uppgiften för fler poäng. Jag antar att där ställs en kombination av kunskaperna om de olika verktygen vi använt under momentet på prov, det känner jag kunde varit mycket användbart och förberedande för vad som komma ska. En bra labb i sin helhet, tack!</p>

