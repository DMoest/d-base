<p class="report__answer">Dagens lärande är inom PHP, precis som tidigare. Den här gången är det mer specifikt under övningen där navigeringslänken markeras för att visa användaren vilken sida hen befinner sig på. Uträkningarna av laddningstid, antalfiler som laddades och använt minne var även givande och roliga övningar som klart och tydligt påvisar mindre enkla användnings områden för PHP. Iterationer och if satser kom till användning på ett utmanande sätt via labben och det gav en bättre förståelse. Ser fram emot fler exempel på praktiska tillämpningar av kunskapen och kommande kursmoment.</p>

